package com.example.school_system.entity;

public class Shopping {
    private int id;
    private String shoppingName;
    private String shoppingPrice;
    private String shoppingPic;
    private String shoppingDesc;
    private String shoppingAddress;
    private String shoppingPhone;
    private int shoppingId;

    public Shopping() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getShoppingName() {
        return shoppingName;
    }

    public void setShoppingName(String shoppingName) {
        this.shoppingName = shoppingName;
    }

    public String getShoppingPrice() {
        return shoppingPrice;
    }

    public void setShoppingPrice(String shoppingPrice) {
        this.shoppingPrice = shoppingPrice;
    }

    public String getShoppingPic() {
        return shoppingPic;
    }

    public void setShoppingPic(String shoppingPic) {
        this.shoppingPic = shoppingPic;
    }

    public String getShoppingDesc() {
        return shoppingDesc;
    }

    public void setShoppingDesc(String shoppingDesc) {
        this.shoppingDesc = shoppingDesc;
    }

    public String getShoppingAddress() {
        return shoppingAddress;
    }

    public void setShoppingAddress(String shoppingAddress) {
        this.shoppingAddress = shoppingAddress;
    }

    public String getShoppingPhone() {
        return shoppingPhone;
    }

    public void setShoppingPhone(String shoppingPhone) {
        this.shoppingPhone = shoppingPhone;
    }

    public int getShoppingId() {
        return shoppingId;
    }

    public void setShoppingId(int shoppingId) {
        this.shoppingId = shoppingId;
    }
}
